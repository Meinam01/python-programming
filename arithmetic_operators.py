a = 15
b = 4

#addition (+)
print("addition:",a + b)

#subtraction(-)
print("subtraction:",a - b)

#multipliction (*)
print("multiplication:",a * b)

#true Division(float result)
print("division:",a / b)

#floor division(integer floor)//
print("floor division:",a // b)

#modulus(remainder) %
print("modulus:",a % b)

#exponential(power)**
print("exponential:",a ** b)


