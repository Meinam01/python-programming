a = 5
b = 3

# and operator
print("a > 2 and b > 1:",a > 2 and b > 1) #true
print("a > 2 and b > 5:",a > 2 and b > 5) #false

# or operator
print("a > 2 or b > 5:",a > 2 or b > 5) #true
print("a < 1 or b < 0:",a < 1 or b < 0) #false

# not operator
print("not(a > 2):",not(a > 2)) #true
print("not(a > 2):",not(a > 5)) #false

